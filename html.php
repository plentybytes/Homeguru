<h2 id="current-month-june-2015-data">Current month (June 2015 data)</h2>

<p>These include the transactions received at Land Registry in the period from the first to the last day of the corresponding month. The data is updated monthly and the average size of this file is 11 MB.</p>

<ul>
  <li>download the most current monthly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-monthly-update.txt">text file</a>
</li>
  <li>download the most current monthly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-monthly-update-new-version.csv">CSV file</a>
</li>
</ul>

<h2 id="single-file">Single file</h2>

<p>These include the transactions received at Land Registry from 1 January 1995 to the most current monthly data. The data is updated monthly and the average size of this file is 2.86 GB.</p>

<ul>
  <li>download the complete transaction data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-complete.txt">text file</a>
</li>
  <li>download the complete transaction data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-complete.csv">CSV file</a>
</li>
</ul>

<h2 id="yearly-file-2015">Yearly file: 2015</h2>

<p>These include the transactions received at Land Registry in the period from 1 January to 31 December for each year. The data is updated monthly and the sizes of these files range from 87 MB to 222 MB. If you are having trouble downloading any of the year files in full, they are also available as two smaller, evenly split CSV files.</p>

<ul>
  <li>download the complete yearly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-2015.txt">text file</a>
</li>
  <li>download the complete yearly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-2015.csv">CSV file (complete)</a>
</li>
  <li>download part 1 of the yearly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-2015-part1.csv">CSV file (part 1)</a>
</li>
  <li>download part 2 of the yearly data as a <a rel="external" href="http://publicdata.landregistry.gov.uk/market-trend-data/price-paid-data/a/pp-2015-part2.csv">CSV file (part 2)</a>
</li>
</ul>

<p>View <a rel="external" href="http://data.gov.uk/dataset/land-registry-monthly-price-paid-data">previous year’s data</a></p>

<p>You can also access the Price Paid Data (<abbr title="Price Paid Data">PPD</abbr>) as machine-readable <a rel="external" href="http://landregistry.data.gov.uk/#linked-data">linked data</a>.</p>

<p>The <a rel="external" href="http://landregistry.data.gov.uk/app/ppd">Paid Data report builder</a> allows you to build bespoke reports using our detailed <abbr title="Price Paid Data">PPD</abbr>. Reports can be based on location, estate type, price paid or property type over a defined period of time.</p>

<h2 id="when-using-or-publishing-our-price-paid-data">When using or publishing our Price Paid Data</h2>

<div class="call-to-action">
<p>You will need to add the following attribution statement if you use or publish our price paid data:</p>

<p><br>
Data produced by Land Registry © Crown copyright 2015.</p>
</div>

<p>When you publish the data, be sure to include information about the nature of the data and any relevant dates for the period of time covered.</p>

<p>If you have found an error with the data please contact Land Registry using our <a rel="external" href="http://landregistry.custhelp.com/app/contactus_changesales/">change a sales record form</a>.</p>

<p>Price Paid information is not personal but property related information. All commercial transactions, sales that have not been lodged with Land Registry or sales that were not for full market value are excluded.</p>

<p>We strive to ensure that our public data is as accurate as possible but cannot guarantee that it is free from errors or fit for your purpose or use. Reports are based on data collected at the time a property transaction is registered with us and will not necessarily be up to date with the most recent information. See <a href="https://www.gov.uk/government/publications/land-registry-data/public-data#accuracy-of-the-data">accuracy of the data</a> for more information.</p>

<p>See <a href="https://www.gov.uk/about-the-price-paid-data#data-excluded-from-the-house-price-index-and-price-paid-data">Data excluded from the House Price Index and Price Paid Data</a> for more information.</p>

